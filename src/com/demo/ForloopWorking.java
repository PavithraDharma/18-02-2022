//Simple working of for loop
package com.demo;
public class ForloopWorking {
	public static void main(String[] args) {
		for(int i=0;i<=3;i++)
		{
			System.out.println("I="+i);
			for(int j=0;j<=2;j++)
			{
				System.out.println("J="+j);
			}
			System.out.println("-------");
		}
	}
}
